import math

# Значения переменных:
M0 = 1537 #Масса полезной нагрузки
Ii = [256, 248, 326, 340] #Удельный импульс двигателя для i-ой ступени
M1i = [173300, 100000, 25450, 4750] #Масса заправленной i-ой ступени,кг
M2i = [15000, 6000, 2710, 1250] #Масса i-ой ступени без топли
N = 4

# Подсчет скорости 
v = 0
for i in range(N):
    v += Ii[i] * math.log((M0 + sum(M1i[i:])) / (M0 + M2i[i] - M1i[i] + sum(M1i[i:])))
    print(v)